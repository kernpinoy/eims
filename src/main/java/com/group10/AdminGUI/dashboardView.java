    private void userView_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_userView_SearchActionPerformed
        SwingUtilities.invokeLater(() -> {
            String sql = "SELECT * FROM `TblUsers` WHERE `user_id` = ?";
            
            try {
                DefaultTableModel model = (DefaultTableModel) userView_table.getModel();
                model.setRowCount(0);
                
                PreparedStatement ps = DBConn.getConn().prepareStatement(sql);
                ps.setString(1, userView_SearchTextField.getText());
                ResultSet rs = ps.executeQuery();
                ResultSetMetaData metadata = rs.getMetaData();
                int columnCount = metadata.getColumnCount();
                
                while (rs.next()) {
                    Vector<String>column = new Vector<String>();
                
                    for (int i = 1; i <= columnCount; i++) {
                        column.add(rs.getString("user_id"));
                        column.add(rs.getString("name"));
                        column.add(rs.getString("contact_number"));
                        column.add(rs.getString("job_dept"));
                        column.add(rs.getString("position"));
                        column.add(rs.getString("username"));
                        column.add(rs.getString("password"));
                        column.add(rs.getString("user_type"));
                    }
                
                    model.addRow(column);
                } 
            } catch(SQLException ex) {
                JOptionPane.showMessageDialog(this, ex, "Error!",
                    JOptionPane.ERROR_MESSAGE);
            }
        });
    }//GEN-LAST:event_userView_SearchActionPerformed

