    private void userView_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_userView_DeleteActionPerformed
        DefaultTableModel model = (DefaultTableModel) userView_table.getModel();
        int selectedRow = userView_table.getSelectedRow();
        
        // get the user_id of the selected row
        String id = String.valueOf(model.getValueAt(selectedRow, 0));
        String username = String.valueOf(model.getValueAt(selectedRow, 5));
        String password = String.valueOf(model.getValueAt(selectedRow, 6));
        
        if (id.isBlank()) {
            JOptionPane.showMessageDialog(this, "Select the entry to be deleted first"
                    , "Warning!", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // dont allow to delete self
        if (UserLogin.getUsername().equals(username) && UserLogin.getPassword().equals(password)) {
            JOptionPane.showMessageDialog(this, "You cannot delete your own account!"
                    , "Warning!", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        //SQL Statement
        String sql = "DELETE FROM `TblUsers` WHERE `user_id` = ?";
        
        SwingUtilities.invokeLater(() -> {
            try {
                Connection conn = DBConn.getConn();
                PreparedStatement ps = DBConn.getPs();
                
                ps = conn.prepareStatement(sql);
                ps.setString(1, id);
                
                ps.executeUpdate();
                
                // rearrange login_id
                String[] arrange = {"SET @count = 0", "UPDATE `TblUsers` SET `user_id` = @count:= @count + 1",
                "ALTER TABLE `TblUsers` AUTO_INCREMENT = 1"};
                Statement st = conn.createStatement();
                for (String query : arrange) {
                    st.addBatch(query);
                } 
                
                st.executeBatch();
                
                SwingUtilities.invokeLater(() -> updateAccTable());
                
                JOptionPane.showMessageDialog(this, 
                        "Entry deleted!", "Success!!"
                        , JOptionPane.INFORMATION_MESSAGE);
            } catch(SQLException ex) {
                JOptionPane.showMessageDialog(this, ex, "Error!"
                        , JOptionPane.ERROR_MESSAGE);
            }
        });
    }//GEN-LAST:event_userView_DeleteActionPerformed

