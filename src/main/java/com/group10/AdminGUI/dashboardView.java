    private void updateAccTable() {     
        String sql = "SELECT * FROM `TblUsers`";
        Connection conn = DBConn.getConn();
        PreparedStatement ps = DBConn.getPs();
        ResultSet rs = DBConn.getRs();
        ResultSetMetaData metadata = DBConn.getMetaData();
        DefaultTableModel RecordTable = (DefaultTableModel) userView_table.getModel();
        RecordTable.setRowCount(0);
        int columnCount;
        
        try {
            ps = conn.prepareStatement(sql);  
            rs = ps.executeQuery();
            metadata = rs.getMetaData();
            columnCount = metadata.getColumnCount();
            
            while (rs.next()) {
                Vector<String>column = new Vector<String>();
                
                for (int i = 1; i <= columnCount; i++) {
                    column.add(rs.getString("user_id"));
                    column.add(rs.getString("name"));
                    column.add(rs.getString("contact_number"));
                    column.add(rs.getString("job_dept"));
                    column.add(rs.getString("position"));
                    column.add(rs.getString("username"));
                    column.add(rs.getString("password"));
                    column.add(rs.getString("user_type"));
                }
                
                RecordTable.addRow(column);
            }
        } catch(SQLException ex) {
            JOptionPane.showMessageDialog(this, ex, "Error!",
                    JOptionPane.ERROR_MESSAGE);
        }
    }
    
